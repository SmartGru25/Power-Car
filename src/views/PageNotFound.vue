<template>
   <div>  
      <div
         v-if="$store.state.network.id == 820"
         align="center"
         width="100vw"
      >
      <h1 class="myFont mt-10">Page Not Found</h1>
      <h2 class="myFont">...but you found a car.</h2>
         <v-card
         align="center"
         width="250px"
         min-height="300px"
         class="ma-10"
         :style="$store.state.wallet.address == owner ? 'border: 2px solid green;' : 'border: 2px solid red;'"
      >
         <div>   
            <v-btn 
            class="float-sm-left mb-2" 
            shapped
            color="blue"
            width="100%"
            >
            CAR 404
            </v-btn>

            <img 
            width="85%"
            :src="imagePath"
            >
            <p :title="owner">
            Owner: {{owner.substring(0, 4)+"..."+owner.substring(owner.length -4, owner.length)}}
            </p>
                  
      <p>
         <span 
           v-if="total>0"
           :class="winRateColor()"
         >Win Rate: {{(wins*100/total).toFixed(0)}}% </span>
        <span v-if="bonus>0"> Bonus: +{{bonus}}</span><br>
        <span v-if="carState == 1 && $store.state.wallet.address == owner">Price: {{carPrice}}</span>
      </p>
      
         </div>

         <v-btn
            class="black--text" 
            v-if="carState == 1 && $store.state.wallet.address != owner"
            color="yellow"
            @click="buyCar()"
            >
            BUY ({{carPrice.toFixed()}} CLO)
            </v-btn>

            <v-overlay
               :absolute="absolute"
               :value="sellOverlay"
               opacity="90"
            >
            <p>Fee: {{$store.state.feeRate}}%</p>
            <p>Set the CAR price:</p>
            <v-text-field
               v-model="amount"
               background-color="black"
               color="yellow"
               solo
               class="ml-0"
               outlined
               hide-details
               type="number"
               step="1"
               min="1"
               dense
            >
            </v-text-field>
               <v-btn
                  color="success"
                  @click="sellCar()"
               >
                  Confirm
               </v-btn>
               <v-btn
                  color="error"
                  @click="sellOverlay = false"
               >
                  Cancel
               </v-btn>
            </v-overlay>



            <v-btn
            v-if="carState == 0 && $store.state.wallet.address == owner"
            class="mr-0 black--text" 
            color="yellow"
            @click="sellOverlay = true"
            width="50%"
            >
            SELL
            </v-btn>

         <v-btn
            class=" black--text" 
            v-if="carState == 1 && $store.state.wallet.address == owner"
            color="yellow"
            @click="cancelSell()"
            width="100%"
         >
            CANCEL SELL
         </v-btn>







      



         <v-overlay
            :absolute="absolute"
            :value="progressOverlay"
            opacity="90"
         >
         <img src="@/assets/Fuel.gif">
         </v-overlay>
      </v-card>
      </div>  
      
      <error-overlay
      v-if="$store.state.network.id != 820"
      />
   </div>
</template>

<script>
import Web3 from 'web3'
export default {
    props: ['car'],
    data() {
      return {
        imagePath: '',
        carState: 0,
        owner: '',
        carPrice: 0,
        amount: 0,
        betCar: 0,
        absolute: true,
        sellOverlay: false,
        raceOverlay: false,
        progressOverlay: false,
        bonus: 0,
        wins: 0,
        total: 0
      }
    },
    beforeMount(){
        this.$store.state.contract.methods.tokenURI(404).call((err, res) => {
          this.imagePath = res
        })
        this.$store.state.contract.methods.carBonus(404).call((err, res) => {
          this.bonus = res
        })
        this.$store.state.contract.methods.carWinRate(404).call((err, res) => {
          this.wins = res.wins
          this.total = res.total
        })
        this.$store.state.contract.methods.carState(404).call((err, res) => {
          this.carState = res;
          if(res == 1){
            this.$store.state.contract.methods.carSales(404).call((err, res) => {
              this.carPrice = res.carPrice/(10**18)
            })
          }
        })
        this.$store.state.contract.methods.ownerOf(404).call((err, res) => {
          this.owner = res
        })
    },
    methods: {
      winRateColor(){
         let winRate = this.wins*100/this.total
         if(winRate <= 100 && winRate > 66){
            return "green--text"
         }
         if(winRate <= 65 && winRate > 33){
            return "yellow--text"
         }
         if(winRate <= 33 && winRate >= 0){
            return "red--text"
         }
      },
      sellCar () {
        this.sellOverlay = false;
        this.progressOverlay = true;
         this.$store.state.contract.methods.createCarSale(404, this.amount).send({from: this.$store.state.wallet.address})         
        .then(value => {
          sessionStorage.setItem("lastTx", value.transactionHash) 
          location.reload()
        })
        .catch(err => {
          this.$store.commit('showSnackbar', err.message);
          this.progressOverlay = false;
          });

      },
      cancelSell () {
        this.progressOverlay = true;
        this.$store.state.contract.methods.endSale(404).send({from: this.$store.state.wallet.address})         
        .then(value => {
          sessionStorage.setItem("lastTx", value.transactionHash) 
          location.reload()
        })
        .then(value => { console.log(value) })
        .catch(err => {
          this.$store.commit('showSnackbar', err.message);
          this.progressOverlay = false;
          });
      },
      buyCar () {
        this.progressOverlay = true;
        var web3 = new Web3(window.ethereum || Web3.givenProvider);
        let amountToSend = web3.utils.toWei(this.carPrice+'', "ether"); 
        this.$store.state.contract.methods.buyCar(404).send({from: this.$store.state.wallet.address, value: amountToSend})         
        .then(value => {
          sessionStorage.setItem("lastTx", value.transactionHash) 
          location.reload()
        })
        .catch(err => {
          this.$store.commit('showSnackbar', err.message);
          this.progressOverlay = false;
          });
      },
      createRace () {
        this.progressOverlay = true;
        this.$store.state.contract.methods.createDragRace(404).send({from: this.$store.state.wallet.address})         
        .then(value => {
          sessionStorage.setItem("lastTx", value.transactionHash) 
          location.reload()
        })
        .catch(err => {
          this.$store.commit('showSnackbar', err.message);
          this.progressOverlay = false;
          });
      },
      cancelRace () {
        this.progressOverlay = true;
        this.$store.state.contract.methods.cancelRace(404).send({from: this.$store.state.wallet.address})         
        .then(value => {
          sessionStorage.setItem("lastTx", value.transactionHash) 
          location.reload()
        })
        .catch(err => {
          this.$store.commit('showSnackbar', err.message);
          this.progressOverlay = false;
          });
      },
      acceptRace () {
        this.raceOverlay = false;
        this.progressOverlay = true;
        this.$store.state.contract.methods.acceptDragRace(404, this.betCar).send({from: this.$store.state.wallet.address})         
        .then(value => {
          sessionStorage.setItem("lastTx", value.transactionHash) 
          location.reload()
        })
        .catch(err => {
          this.$store.commit('showSnackbar', err.message);
          this.progressOverlay = false;
          });
      }
    },
    components: {
      'error-overlay': require('@/components/Shared/ErrorOverlay.vue').default,
    }
   
    }


</script>

